// ============================================================
// 1. ПОДКЛЮЧАЕМ БИБЛИОТЕКИ
// ============================================================

// Подключаем Express — фреймворк для создания сервера
const express = require('express');

// Подключаем Path — встроенный модуль для работы с путями
const path = require('path');

// ============================================================
// 2. СОЗДАЕМ ПРИЛОЖЕНИЕ
// ============================================================

// Создаем экземпляр приложения Express
const app = express();

// Определяем порт, на котором будет работать сервер
const PORT = process.env.PORT || 3000;

// ============================================================
// 3. НАСТРАИВАЕМ РАЗДАЧУ СТАТИЧЕСКИХ ФАЙЛОВ
// ============================================================

// Говорим Express: "Все файлы из папки public отдавай, 
// когда кто-то их запрашивает"
app.use(express.static('public'));

// ============================================================
// 4. ДОБАВЛЯЕМ ДОПОЛНИТЕЛЬНЫЙ МАРШРУТ (НЕОБЯЗАТЕЛЬНО)
// ============================================================

// Если пользователь зайдет на http://localhost:3000/api
// покажем ему JSON с информацией
app.get('/api', (req, res) => {
    // req — объект запроса (что прислал пользователь)
    // res — объект ответа (что отправляем обратно)
    res.json({
        message: 'Добро пожаловать в API моего резюме!',
        author: 'Александра Жавнеронок',
        role: 'Junior C++ Developer',
        projects: ['Курсовая работа: Библиотека', 'Дебют 2025']
    });
});

// ============================================================
// 5. ЗАПУСКАЕМ СЕРВЕР
// ============================================================

// Запускаем сервер на порту 3000
app.listen(PORT, () => {
    // Эта функция выполнится, когда сервер запустится
    console.log(`Сервер запущен: http://localhost:${PORT}`);
    console.log('Нажмите Ctrl+C для остановки');
});